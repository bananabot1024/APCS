public class Employee {

	private String name;
	
	/**
	 * Constructor to initialize instance variable name
	 * @param name - name of employee
	 */
	public Employee(String name) {
		this.name = name;
	}
	
	/**
	 * Getter method for name
	 * @return name
	 */
	public String getName() {
		return name;
	}
	
}
